public class Slide40a {
  public static void main (String[] args) {
    System.out.println("== 'do .. while' loop ==");
    int i = 0;
    do {
      i++;
      System.out.println("i = " + i);
    } while (i < 3);

    System.out.println("== 'while' loop ==");
    int j = 0;
    while (j < 3) {
      j++;
      System.out.println("j = " + j);
    }
  }
}
