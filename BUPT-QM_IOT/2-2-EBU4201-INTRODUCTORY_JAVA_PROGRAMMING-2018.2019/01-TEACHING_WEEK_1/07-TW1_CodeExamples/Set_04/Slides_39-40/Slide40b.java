public class Slide40b {
  public static void main (String[] args) {
    System.out.println("== 'do .. while' loop ==");
    int i = 3;
    do {
      System.out.println("i = " + i);
      i++;
    } while (i < 3);

    System.out.println("== 'while' loop ==");
    int j = 3;
    while (j < 3) {
      System.out.println("j = " + j);
      j++;
    }
  }
}
